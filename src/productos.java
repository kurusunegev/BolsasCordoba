    void cargar(String valor){
        String [] titulos ={"id","marca","precio","modelo","tamaño","color"}; 
        String [] registros = new String[9];
        String sql = "SELECT * FROM productos WHERE CONCAT(marca ) like '%"+valor+"%'";
        model = new DefaultTableModel(null, titulos);
                
        conectar cc = new conectar();
        Connection cn =cc.conexion();
        
        try {
            Statement  st =cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()){
                registros[0]=rs.getString("id");
                registros[1]=rs.getString("marca");
                registros[2]=rs.getString("precio");
                registros[3]=rs.getString("modelo");
                registros[4]=rs.getString("tamaño");
                registros[5]=rs.getString("color");
                
                
                model.addRow(registros);
            
                tdatos.setModel(model);
                
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    

        
    }
    
