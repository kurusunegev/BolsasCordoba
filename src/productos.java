    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        conectar c = new conectar();
        Connection cn = c.conexion();

        try{
            String sql = "UPDATE productos SET marca=?, precio=?, modelo=?, tamaño=?, color=? " +
            "WHERE id=?";
            int fila = tdatos.getSelectedRow();
            String dato =(String) tdatos.getValueAt(fila, 0);
            PreparedStatement pst = cn.prepareStatement(sql);
            pst.setString(1, txtmarca.getText());
            pst.setString(2, txtprecio.getText());
            pst.setString(3, txtmodelo.getText());
            pst.setString(4, txttamaño.getText());
            pst.setString(5, cbcolor.getSelectedItem().toString());
            
            pst.setString(6, dato);

            int n = pst.executeUpdate();
            if(n>0){

                cargar("");
                limpiar();
                JOptionPane.showMessageDialog(null, "Datos Modificados");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());

        }
    }//GEN-LAST:event_jButton1ActionPerformed

